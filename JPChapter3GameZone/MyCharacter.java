public class MyCharacter {
    String name;
    int lives;
    String species;

    public String getName(){
        return name;
    }

    public int getLives(){
        return lives;
    }

    public String getSpecies(){
        return species;
    }

    public void setName(String name){
        this.name = name;
    }

    public void setLives(int lives){
        this.lives = lives;
    }

    public void setSpecies(String species){
        this.species = species;
    }
}

